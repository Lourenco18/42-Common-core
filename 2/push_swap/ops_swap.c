#include "push_swap.h"

void	sa(t_node **a)
{
	t_node *x;

	if (!*a || !(*a)->next)
		return ;
	x = (*a)->next;
	(*a)->next = x->next;
	x->next = *a;
	*a = x;
	write(1, "sa\n", 3);
}